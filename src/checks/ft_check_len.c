#include "../../ft_printf.h"

int	ft_check_len(size_t *i, const char *str)
{
	if (str[*i] == 'h' || str[*i] == 'l' || str[*i] == 'L' || str[*i] == 'z'
	|| str[*i] == 'j' || str[*i] == 't')
		return (1);
	return (0);
}